#include "libft.h"

int		ft_wordcnt(char const *s, char c)
{
	int		i;
	int		n;
	int		res;

	i = 0;
	n = 0;
	res = 0;
	if (s[i] == '\0')
		return (0);
	while (s[i] != '\0')
	{
		if ((s[i]) == c)
		{
			i++;
			n = 0;
		}
		else
		{
			if (n == 0)
				res++;
			n = 1;
			i++;
		}
	}
	return (res);
}
