#include "libft.h"

size_t	ft_intlen(int n)
{
	int	res;

	res = 0;
	if (n < 0)
	{
		if (n == -2147483648)
		{
			res++;
			n = -147483648;
		}
		res++;
		n = -n;
	}
	if (n == 0)
	{
		res = 1;
	}
	while (n > 0)
	{
		res++;
		n = n / 10;
	}
	return (res);
}
